<b>Art is a diverse range of human activities in creating visual, auditory or performing artifacts (artworks), expressing the author's imaginative, conceptual idea, or technical
 skill, intended to be appreciated for their beauty or emotional power.In their most general form these activities include the production of works of art, the criticism of 
 art, the study of the history of art, and the aesthetic dissemination of art.
Music, theatre, film, dance, and other performing arts, as well as literature and other media such as interactive media, are included in a broader definition of art or the arts.
 Until the 17th century, artreferred to any skill or mastery and was not differentiated from craftsor sciences. In modern usage after the 17th century, where aesthetic considerations
 are paramount, the fine arts are separated and distinguished from acquired skills in general, such as the decorative or applied arts.
Though the definition of what constitutes art is disputed and has changed over time, general descriptions mention an idea of imaginative or technical skill stemming from human 
agency and creation. The nature of art and related concepts, such as creativity and interpretation, are explored in a branch of philosophy known as aesthetics.<br><br><br><br>

<h3>Creative art and fine art</h3><br><br>
In the perspective of the history of art, artistic works have existed for almost as long as humankind: from early pre-historic artto contemporary art; however, some theories
 restrict the concept of "artistic works" to modern Western societies. One early sense of the definition of art is closely related to the older Latin meaning, which roughly
 translates to "skill" or "craft," as associated with words such as "artisan." English words derived from this meaning include artifact, artificial, artifice, medical arts, and military
 arts. However, there are many other colloquial uses of the word, all with some relation to its etymology.
Over time, philosophers like Plato, Aristotle, Socrates and Kant, among others, questioned the meaning of art.Several dialogues in Plato tackle questions about art: Socrates says
 that poetry is inspired by the muses, and is not rational. He speaks approvingly of this, and other forms of divine madness (drunkenness, eroticism, and dreaming) in the Phaedrus
, and yet in the Republic wants to outlaw Homer's great poetic art, and laughter as well. In Ion, Socrates gives no hint of the disapproval of Homer that he expresses in the Republic.
 The dialogue Ion suggests that Homer's Iliad functioned in the ancient Greek world as the Bible does today in the modern Christian world: as divinely inspired literary art that can 
 provide moral guidance, if only it can be properly interpreted.
With regards to the literary art and the musical arts, Aristotle considered epic poetry, tragedy, comedy, dithyrambic poetry and music to be mimetic or imitative art, each varying in 
imitation by medium, object, and manner.For example, music imitates with the media of rhythm and harmony, whereas dance imitates with rhythm alone, and poetry with language. 
The forms also differ in their object of imitation. Comedy, for instance, is a dramatic imitation of men worse than average; whereas tragedy imitates men slightly better than average.
 Lastly, the forms differ in their manner of imitationâ€”through narrative or character, through change or no change, and through drama or no drama.Aristotle believed that imitation
 is natural to mankind and constitutes one of mankind's advantages over animals.
The second, and more recent, sense of the word art as an abbreviation for creative art or fine art emerged in the early 17th century. Fine art refers to a skill used to express the
 artist's creativity, or to engage the audience's aesthetic sensibilities, or to draw the audience towards consideration of more refined or finer work of art.
