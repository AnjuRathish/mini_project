<h4>Folkloric Dances<h4><br>
Ron Houston, one of the founders of The Society of Folk Dance Historians, a  research group informally affiliated with The University of Texas at Houston, considered the
 question in a 2012 issue of the group's newsletter. He concluded that there really is no one answer to the question, but in the process of doing so he made some interesting distinctions
 between different kinds of dancing that are often grouped together with the term "folk dancing.' He begins with what is probably the narrowest of "folk dance" categories:folkloric dances.
 These, he proposes, are dances originally performed for "metaphysical purposes" -- religious rites and related ritual behavior.  They persist in the present day as atavistic remainders of a 
 way of life that no longer exists; in that sense, recreated folkloric dances can be almost anything, from scholarly reenactments to dances under entirely different auspices and with very
 different meanings that retain the flavor of a folkloric dance. Vassal Nijinsky's "Rite of Spring," (Le Sacre du Printemps)  with music by Igor Stravinsky is a well-known example of a
 recreated and radically redirected version of a supposedly primitive ritual dance.
