<h3><center>Bharatanatyam</center></h3><br><br> 

<img src="dance1.jpg" alt="Bharatanatyam picture" class="a" />
<br><br>
(Tamil : "பரதநாட்டியம்"), is a major genre of Indian classical dance that originated in Tamil Nadu. Traditionally, Bharatanatyam has been a solo dance that
 was performed exclusively by women, and it expressed South Indian religious themes and spiritual ideas, particularly of Shaivism, but also of Vaishnavism and Shaktism.
Bharatanatyam's theoretical foundations trace to the ancient Sanskrit text by Bharata Muni, Natya Shastra,its existence by 2nd century CE is noted in the ancient Tamil 
epic Silappatikaram, while temple sculptures of 6th to 9th century CE suggest it was a well refined performance art by mid 1st millennium CE.Bharatanatyam may be the oldest
 classical dance tradition of India.
Bharatanatyam style is noted for its fixed upper torso, legs bent or knees flexed out combined with spectacular footwork, a sophisticated vocabulary of sign language based on 
gestures of hands, eyes and face muscles.The dance is accompanied by music and a singer, and typically her guru is present as the director and conductor of the performance and art.
The dance has traditionally been a form of an interpretive narration of mythical legends and spiritual ideas from the Hindu texts.The performance repertoire of Bharatanatyam,
 like other classical dances, includes nrita (pure dance), nritya (solo expressive dance) and natya (group dramatic dance).<br><br>

<h3><center>Ballet</center></h3><br><br> 

<img src="ballet.jpg" alt="ballet picture" class="b" />
<br><br><br>
Ballet originated in the 15th century, first in Italy and then in France. Over the centuries, ballet has influenced many other styles of
 dance and become a fine art form in its own right. There are three basic styles:
•	Classical: This form reached its peak in 19th-century France and Russia. It is often story-driven andorchestrated 
("The Nutcracker" is a great example), with fantastical sets and costumes. The movement emphasizes pointe work (dancing on toes), graceful expressions, and symmetry among dancers.
•	Neoclassical: This is an evolution of classical ballet, which emerged in the early to mid-20th century.
 Movements are faster and more urgent, with less emphasis on symmetry, and simple sets and costumes. Plot is often nonexistent. Orchestras, bands, or soloists may accompany the dancers.
•	Contemporary: Like neoclassical, plot is cast aside in favor of pure movement and physical expression, which may not appear to be dancelike at all.
 Costumes and set designs are frequently simple or abstract. Music or sound work, if used, is often contemporary or experimental in nature.
History
The first ballet was performed over 500 years ago. Men traditionally danced the leading roles, as women were considered too weak to perform the challenging and sometimes daring choreography.
 Female dancers didn't take the stage until years later. Many of the most famous ballets were adapted from fairy tales and folk stories. Some ballets are based on historical events and bible 
 stories. If you've never been to a ballet, you may be surprised by how emotional and moving ballet dancing can be.<br><br>

<h3><center>jazz</center></h3><br><br>

<img src="jazz.jpg" alt="jazz picture" class="a" />
<br><br><br>
Jazz is a lively dance style that relies heavily on originality and improvisation. This style often uses bold, dramatic body movements, including body isolations and contractions.
Jazz dance has its roots in African traditions kept alive by slaves brought to the U.S. Over time, this evolved into a style of street dance that soon moved into the jazz clubs of
the early20th century.
During the big-band era of the 1930s and early '40s, swing dancing and the Lindy Hop became popular expressions of jazz dancing. In the mid- to late 20th century,
 choreographers like Katherine Dunham incorporated these improvisational, physical expressions into their own works.<br><br>
 
<h3><center>Hip-HOP</center></h3><br><br>

<img src="hiphop.jpg" alt="hip-hop picture" class="a" />
<br><br><br>
Another descendant of jazz dance, hip-hop emerged from the streets of New York in the 1970s in the city's African-American and Puerto Rican communities at the same time as rap and DJing. 
Breakdancing—with its popping, locking, and athletic floor movements—is perhaps the earliest form of hip-hop dance. Often, "crews" of teams of dancers would hold competitions to see which
 group had bragging rights as the best.
As rap music flourished and diversified, different styles of hip-hop dancing emerged. Krumping and clowning took the physical exuberance of breakdancing and 
added narrative and comic expression in the '90s. In the 2000s, jerkin' and juking became popular; both of these take the pop-lock movement of classic breakdancing and add wild fashions.
